		<h3>Landing/Main Page</h3>
		<ol>
		<li>Add a lot more information about the conference topic.</li>
		<li>Make sure the conference logo/image isn’t too big or move it down to the bottom so that it doesn’t take up too much space when first arriving on the landing page.</li>
		<li>Make Call For Paper (CFP) available from the landing page.</li>
		<li>PC list should be ordered so that it's not all men in the beginning/first viewable half of the page.</li>
		<li>Add a link to the “How to submit” page in the landing page (see First Timers).</li>
		<li>Change “Tracks” to “Submitting” in the main menu in the landing page (if the drop downs there are about submitting to the different tracks).</li>
		<li> Have the main menu items ordered based on temporality of tasks (e.g., first have “Submitting” information instead of “Attending” in the menu).</li>
		<li>Track orders should be consistent between the menu and the “How to submit” page. </li>
		</ol>

		<h3>First Timers</h3>
		<ol>
		<li>Add a how-to-submit paper page, modeled after the ASE’19 site.</li> 
		<li>Make sure Acronyms (NIER, JSEET, etc.) are spelled out.</li>
		<li>Post sample papers accepted at previous conferences. </li>
		<li>Have a section/page geared towards 1st time submitters (e.g. to help decide to attend).</li>
		<li>For double-blind instructions, you can use: http://chi2019.acm.org/authors/papers/chi-anonymisation-policy/</li>
		<li>Make clear how authors of specific tracks can submit papers (links to the submission site).</li>
		</ol>

		<h3>Registration</h3>
		<ol>
		<li>In the visa and registration pages, add information about whether the cost of registration would be refunded if visa is denied.</li>
		<li>The registration link/button needs to be clearly visible on the screen.</li>
		</ol>

		<h3>Program (if a large conference like ICSE)</h3>
		<ol>
		<li>Need some way to help first timers navigate sessions/understand the program in a lightweight way.  </li>
		<li>Advice on how to create your own program (e.g. instructions for navigation, instructions on how to see what others are favoring).</li>
		<li>“Programmer picks”: section of various program committee members saying what they think is going to be particularly interesting at the upcoming site/event.</li>
		<li>More structuring for going through the sessions (e.g., filtering through days, session timeline to see session topics at a glance, filtering through tracks, filtering through industry program).</li>
		<li>Emphasize session titles and not track type.</li>
		<li>Instructions on where to find room (e.g., click on a room and see a map that highlights: location of room? add map to the page?).</li>
		<li>A way to filter through different tracks to make it easier to find sessions.</li>
		<li>Relate D&I (Diversity and Inclusion) to mentoring/social events (e.g, link to mentoring page as in ICSE’18).</li>
		</ol>

		<h3>General</h3>
		<ol>
		<li>Make sure D&I (Diversity and Inclusion plan) is aimed at population that wants to feel included and not too boilerplate</li>
		<li>Have pages organized to facilitate the tasks the user needs to do in that page and not just have to read. Information only text should be condensed.</li>
		<li>Multi-step actions (e.g., registration) should have progress bars.</li>    
		<li>Ability to save data/progress when multiple steps are involved, with this information given at the start of the page.</li>  
		</ol>

	
